function setupSorting() {
    document.getElementById('sortName').addEventListener('click', () => {console.log('Sort by Name clicked'); sortTableByColumn(0);});
    document.getElementById('sortAge').addEventListener('click', () => sortTableByColumn(1, true));
    document.getElementById('sortTeam').addEventListener('click', () => sortTableByColumn(2));
    document.getElementById('sortGames').addEventListener('click', () => sortTableByColumn(3, true));
    document.getElementById('sortWar').addEventListener('click', () => sortTableByColumn(4, true));
    document.getElementById('sortOPSplus').addEventListener('click', () => sortTableByColumn(5, true));
    document.getElementById('sortxwOBA').addEventListener('click', () => sortTableByColumn(6, true));
    document.getElementById('sortSalary').addEventListener('click', () => sortTableByColumn(7, true));
    document.getElementById('sortWarPerMillion').addEventListener('click', () => sortTableByColumn(8, true));
}

function sortTableByColumn(columnIndex, isNumeric = false) {
    const columnKeys = ['Name', 'Age', 'Tm', 'G', 'bWAR', 'OPS+', 'xwOBA', 'Salary'];
    const table = document.getElementById('playersTable');
    const th = table.querySelectorAll('th');

    const compare = (a, b) => {
        let valA, valB;
        if (columnIndex === 8) { 
            valA = calculateWARPerMillion(a.bWAR, a.Salary);
            valB = calculateWARPerMillion(b.bWAR, b.Salary);
        } else {
            valA = isNumeric ? parseFloat(a[columnKeys[columnIndex]]) : a[columnKeys[columnIndex]].toLowerCase();
            valB = isNumeric ? parseFloat(b[columnKeys[columnIndex]]) : b[columnKeys[columnIndex]].toLowerCase();
        }

        if (valA < valB) return -1;
        if (valA > valB) return 1;
        return 0;
    };

    if (!table.sortedColumns) {
        table.sortedColumns = {};
    }

    if (table.sortedColumns[columnIndex] === 'desc') {
        currentDisplayedData.sort(compare);
        table.sortedColumns[columnIndex] = 'asc';
    } else if (table.sortedColumns[columnIndex] === 'asc') {
        if (filteredData) {
            currentDisplayedData = [...filteredData];
        } else {
            currentDisplayedData = [...originalData];
        }
        table.sortedColumns[columnIndex] = 'reset';
    } else {
        currentDisplayedData.sort(compare).reverse();
        table.sortedColumns[columnIndex] = 'desc';
    }

    // Update header class
    th.forEach(header => {
        header.classList.remove('sorted-asc', 'sorted-desc');
    });

    if (table.sortedColumns[columnIndex] !== 'reset') {
        const classToAdd = table.sortedColumns[columnIndex] === 'desc' ? 'sorted-desc' : 'sorted-asc';
        th[columnIndex].classList.add(classToAdd);
    }

    displayPage(1, currentDisplayedData);
}


let currentPage = 1;
const rowsPerPage = 50;

function calculateWARPerMillion(war, salary) {
    return salary > 0 ? (war / (salary / 1000000)).toFixed(2) : 'N/A';
}

function displayPage(page, data = jsonData) {
    currentPage = page;

    // Function to add a row to the table
    function addRowToTable(player) {
        var table = document.getElementById('tableBody');
        var row = table.insertRow();
        row.insertCell(0).innerHTML = player.Name;
        row.insertCell(1).innerHTML = player.Age;
        row.insertCell(2).innerHTML = player.Tm;
        row.insertCell(3).innerHTML = player.G;
        row.insertCell(4).innerHTML = player.bWAR.toFixed(1);
        row.insertCell(5).innerHTML = player['OPS+'];
        row.insertCell(6).innerHTML = player.xwOBA.toFixed(3);
        row.insertCell(7).innerHTML = `$${player.Salary.toLocaleString()}`;
        row.insertCell(8).innerHTML = calculateWARPerMillion(player.bWAR, player.Salary);
    }
    const startIndex = (page - 1) * rowsPerPage;
    const endIndex = startIndex + rowsPerPage;
    const paginatedItems = data.slice(startIndex, endIndex);

    const tbody = document.getElementById('tableBody');
    tbody.innerHTML = ''; // Clear existing rows

    paginatedItems.forEach(addRowToTable);

    if (data === jsonData) {
        setupPagination(data.length, rowsPerPage);
    }
}

function setupPagination(totalItems, rowsPerPage) {
    const paginationElement = document.getElementById('pagination');
    paginationElement.innerHTML = ''; // Clear existing pagination

    const pageCount = Math.ceil(totalItems / rowsPerPage);

    for (let i = 1; i <= pageCount; i++) {
        const pageButton = document.createElement('button');
        pageButton.innerText = i;
        pageButton.addEventListener('click', () => displayPage(i, jsonData));

        if (currentPage === i) {
            pageButton.classList.add('active');
        }

        paginationElement.appendChild(pageButton);
    }

    // Display pagination element even if there's only one page
    paginationElement.style.display = '';
}

function showSearchSuggestions(event) {
    if (event.key === 'Enter') {
        updateTableWithSearch();
    } else {
        const input = document.getElementById('searchInput');
        const filter = input.value.toLowerCase();
        const searchResults = document.getElementById('searchResults');
        searchResults.innerHTML = ''; // Clear previous search results

        const filteredData = jsonData.filter(player => 
            player.Name.toLowerCase().includes(filter)
        );

        // Show search suggestions
        filteredData.forEach(player => {
            const playerLink = document.createElement('a');
            playerLink.href = '#';
            playerLink.innerText = player.Name;
            playerLink.onclick = function() {
                input.value = player.Name; // Fill input with the clicked player's name
                updateTableWithSearch(); // Update the table with the selected player
                return false; // Prevent default link behavior
            };

            const listItem = document.createElement('div');
            listItem.appendChild(playerLink);
            searchResults.appendChild(listItem);
        });
    }
}

function updateTableWithSearch() {
    const input = document.getElementById('searchInput');
    const filter = input.value.toLowerCase();

    // Perform filtering
    const newFilteredData = jsonData.filter(player => 
        player.Name.toLowerCase().includes(filter)
    );

    // Update filteredData based on whether there are results
    filteredData = newFilteredData.length > 0 ? newFilteredData : null;

    // Decide what to display based on the filter results
    currentDisplayedData = filteredData ? [...filteredData] : [...jsonData];
    
    // Reset to the first page and update the display
    currentPage = 1;
    displayPage(currentPage, currentDisplayedData);
    setupPagination(currentDisplayedData.length, rowsPerPage);
    input.value = ''; 
    document.getElementById('searchResults').innerHTML = '';
}

function clearSearch() {
    document.getElementById('searchInput').value = '';
    filteredData = null;
    currentDisplayedData = [...jsonData];
    displayPage(1, currentDisplayedData);
    setupPagination(jsonData.length, rowsPerPage);
}

// Call displayPage initially to show the first set of data
document.addEventListener('DOMContentLoaded', () => {
    clearSearch();
    setupSorting();
});